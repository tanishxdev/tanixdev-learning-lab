// 1. var – old way (avoid in modern JS)
var username = "John";
var age;

// 2. let – mutable value, block scoped
let score = 100;
let lives = 3;

// 3. const – immutable binding, block scoped
const PI = 3.14;
const BIRTH_YEAR = 2000;

// 4. Multiple declaratio
const x = 10, y = 20, z = 30;
const API_URL = "https://example.com/api", API_KEY = "1234567890", API_SECRET = "abcdefg";

console.log(username);
console.log(age);
console.log(score);
console.log(lives);
console.log(PI);
console.log(BIRTH_YEAR);
console.log(x);
console.log(y);
console.log(z);
console.log(API_URL);
