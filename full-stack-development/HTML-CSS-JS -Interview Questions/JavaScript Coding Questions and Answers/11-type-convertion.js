console.log(1 + "2"); // + operator converts number to string and concatenates
console.log(1 - "2"); // - operator converts string to number and performs subtraction
console.log(1 * "2"); // * operator converts string to number and performs multiplication
console.log(1 / "2"); // / operator converts string to number and performs division
console.log(1 + true); // + operator converts true to 1 and performs addition
console.log(1 + false); // + operator converts false to 0 and performs addition
console.log(1 + null); // + operator converts null to 0 and performs addition
console.log(1 + undefined); // + operator converts undefined to NaN and results in NaN
console.log("5" * "2"); // * operator converts both strings to numbers and performs multiplication
console.log("5" + "2"); // + operator concatenates the two strings
console.log("5" - "2"); // - operator converts both strings to numbers and performs subtraction
console.log("5" / "2"); // / operator converts both strings to numbers and performs division
