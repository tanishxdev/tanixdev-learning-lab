let course = "node";

switch (course) {
  case "javascript":
    console.log("this is javascript course");
    break;
  case "python":
    console.log("this is python course");
    break;
  case "node":
    console.log("this is node course");
    break;
  case "CSS":
    console.log("this is CSS course");
    break;

  default:
    console.log("course not found");
}
