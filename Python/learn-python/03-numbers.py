# data types
a = 10          # integer
b = 3.14        # float
c = "Hello"     # string
d = True        # boolean
e = [1, 2, 3]   # list
f = (4, 5, 6)   # tuple
g = {7, 8, 9}   # set
h = {'key': 'value'}  # dictionary
i = None        # NoneType

# printing data types
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))
print(type(i))

# TRAP
print(5/2)  # In Python 3, this will output 2.5 (float division)
print(5//2) # This will output 2 (floor division)
print(5%2)  # This will output 1 (modulus)
print(2**3) # This will output 8 (exponentiation)

# type conversion
num_str = "100"
num_int = int(num_str)  # converting string to integer
num_float = float(num_int)  # converting integer to float
print(num_int)
print(num_float)
print(num_str)

